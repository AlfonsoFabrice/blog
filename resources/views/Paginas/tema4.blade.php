				<div class="image main"><img src="images/pic01.jpg" alt="" /></div>
				<p>La capacidad de una <b>computadora central</b> se define tanto por la velocidad de su CPU como por su gran memoria interna, su alta y gran capacidad de almacenamiento externo, sus resultados en los dispositivos E/S rápidos y considerables, la alta calidad de su ingeniería interna que tiene como consecuencia una alta fiabilidad y soporte técnico caro pero de alta calidad. Una computadora central puede funcionar durante años sin problemas ni interrupciones y las reparaciones del mismo pueden ser realizadas mientras está funcionando. Los vendedores de computadoras centrales ofrecen servicios especiales; por ejemplo, si se rompe la computadora, el vendedor ejecutará las aplicaciones de su cliente en sus propias computadoras sin que los usuarios lo noten mientras que duran las reparaciones. La independencia interna de estas computadoras es tan grande que, por lo menos, en un caso conocido, los técnicos pudieron cambiar las computadoras centrales de sitio desmontándolas pieza a pieza y montándolas en otro lugar, dejando, mientras tanto, dichas computadoras funcionando; en este ejemplo, el cambio de las computadoras centrales de un sitio a otro se produjo de manera transparente.</p>
				<p>A menudo, las computadoras centrales soportan miles de usuarios de manera simultánea que se conectan mediante falsos terminales. Algunas computadoras centrales pueden ejecutar o dar cobijo a muchos sistemas operativos y por lo tanto, no funcionan como una computadora sola, sino como varias computadoras virtuales. En este papel, una computadora central por sí sola puede remplazar docenas o cientos de pequeñas computadoras personales, reduciendo los costes administrativos y de gestión al tiempo que ofrece una escalabilidad y fiabilidad mucho mejor. La fiabilidad se consigue por la independencia de sus componentes internos señalada anteriormente, y la escalabilidad se logra porque los recursos físicos de la computadora pueden ser redistribuidos entre los terminales virtuales según las necesidades; esto es mucho más difícil de hacer con las computadoras personales, porque para quitar o añadir nuevos componentes físicos hay que desenchufar la computadora muchas veces y las limitaciones de dichos componentes son mucho mayores. Cuando una computadora central actúa como el centro de operaciones de muchos terminales virtuales, puede ofrecer la potencia necesaria para que dichas computadoras operen de manera eficiente, pero también la flexibilidad de las redes de computadoras personales.</p>
				<p><b>Un servidor dedicado</b> es un equipo informático físico que destina todos sus recursos a proporcionar información y atender las peticiones de otro ordenador (cliente) que ha contratado sus servicios. Por tanto, a diferencia de un servidor compartido, el dedicado trabaja en exclusiva para un solo cliente. Existe un amplio abanico de servidores dedicados entre los que pueden elegir para el alojamiento en Internet. Por analogía, podemos decir que un servidor dedicado es un ordenador lo que un chalet en la montaña a su propietario. Este último disfruta de una vivienda sin vecinos a su alrededor, de igual manera que una computadora que tenga un servidor dedicado contratado goza, en soledad, de todos los medios de éste (memoria RAM, CPU, sistema operativo, disco duro, etcétera).</p>
				<h4>Características de un servidor dedicado:</h4>
				<p>Un servidor dedicado se caracteriza, abundante, por ofrecer sus recursos y servicios a un único individuo, aunque también son importantes los siguientes aspectos:</p>
				<ul>
					<li>Se localiza, por regla general, en  un centro de datos , lugar en el que se encuentran todos los recursos que una entidad necesita para procesar su información.</li>
					<li>Proporciona más seguridad y mayor velocidad, ya que los recursos de este gran ordenador central no se comparten con otros usuarios.</li>
					<li>Está destinado a alojar sitios web con un tráfico elevado, siendo recomendable tanto que las que contratan estos servicios reciben un gran número de visitas diarias.</li>
					<li>Posee una alta cuota mensual, superando, incluso, los 100 euros al mes, ya que, entre otras cosas, es un solo cliente el que hace frente a los gastos de mantenimiento.</li>
					<li>Cuenta, en la mayoría de los casos, con un servicio de atención disponible las 24 horas del día y los 365 días del año.</li>
				</ul>
