					<h2>¿Qué es un sistema de información?</h2>
				</header>
				<p>Cuando se habla de un sistema de información (SI) se refiere a un conjunto ordenado de mecanismos que tienen como fin la administración de datos y de información, de manera que puedan ser recuperados y procesados fácil y rápidamente.</p>
				<p>Todo sistema de información se compone de una serie de recursos interconectados y en interacción, dispuestos del modo más conveniente en base al propósito informativo trazado, como puede ser recabar información personal, procesar estadísticas, organizar archivos, etc.</p>
				<h4>Estos recursos pueden ser:</h4>
				<ul>
					<li>Recursos humanos. Personal de variada índole y destrezas.</li>
					<li>Datos. Cualquier tipo de información masiva que precisa de organizarse.</li>
					<li>Actividades. Procedimientos, pasos a seguir, estaciones de trabajo, etc.</li>
					<li>Recursos informáticos. Aquellos determinados por la tecnología.</li>
				</ul>
				<p>Se debe destacar que no es lo mismo un sistema de información que un sistema informático, si bien estos últimos constituyan a menudo el grueso de los recursos de un SI. Pero existen muchos otros métodos para los sistemas de información, que no necesariamente pasan por la informática.</p>
				<p>Por otro lado, la tecnología TI describe cualquier tecnología que impulse o permita el almacenamiento, procesamiento y flujo de comunicación dentro de una organización. Todo lo relacionado con computadoras: software, redes, intranets, sitios web, servidores, bases de datos y telecomunicaciones queda bajo el paradigma de TI.</p>
				<p>La mayoría de las empresas modernas dependen, en gran medida, de los sistemas para la gestión de sus operaciones y la toma de decisiones; desde el correo electrónico, hasta la administración de la base de datos y sitios web.</p>
				<p>La información comienza como flujo de datos sin procesar, representan eventos que ocurren en organizaciones o en el entorno físico antes de que se hayan organizado en una forma que las personas puedan entender y usar: Son la materia prima para el procesamiento y se refieren a hechos, eventos y transacciones. Por lo tanto, el propósito de los SI es convertir los recursos en bruto en información útil que pueda utilizarse para tomar decisiones en una organización. Por ejemplo, los hospitales tienen grandes bases de datos de pacientes para lograr un historial clínico eficiente. Las universidades cuentan con sistemas para el manejo del personal, alumnos, transacciones de pagos, así como redes en expansión para administrar el campus. Incluso una pequeña empresa dedicada a la comida con envíos a domicilio necesita un sistema de gestión y seguimiento de pedidos.</p>
