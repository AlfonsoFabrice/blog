						en la Nube</h1>
				</header>
				<div class="image main"><img src="images/pic01.jpg" alt="" /></div>
				<p>La computación en la nube (o cloud computing, en inglés) es una tecnología que permite acceder remotamente, de cualquier lugar del mundo y en cualquier momento, a softwares, almacenamiento de archivos y procesamiento de datos a través de Internet, sin la necesidad de conectarse a un ordenador personal o servidor local.</p>
				<p>En otras palabras, la computación en la nube utiliza la conectividad y gran escala de Internet para hospedar los más variados recursos, programas e información, y permite que el usuario ingrese a ellos través de cualquier ordenador, tablet o celular.</p>
				<p>Si todavía no quedó claro, déjame ejemplificarlo: si alguna vez editaste un documento en Google Docs, escuchaste música en Spotify o viste una película en Netflix, tuviste contacto directo con la computación en la nube. Todos ellos son servicios online que no demandan la descarga, instalación y acceso de ningún tipo de software o archivo en tu ordenador. Solo necesitas un navegador y una buena conexión a Internet para utilizarlos.</p>
				<p>El cloud computing utiliza un servidor remoto para conectar los dispositivos de los usuarios a recursos centralizados. Un servidor remoto almacena todos los datos y programas que necesitas, y puede estar en tu propio país o al otro lado del mundo, por ejemplo, en Japón. El hecho es que logras accederlos por Internet, desde cualquier lugar, aunque estén almacenados a kilómetros de distancia.</p>
				<p>Hay 3 tipos de computación en la nube: la pública, la privada y la híbrida. Si tienes una empresa y piensas utilizar un servicio en la nube, ten en mente que la elección dependerá de factores relacionados con el costo, la disponibilidad, el rendimiento y las expectativas.</p>
