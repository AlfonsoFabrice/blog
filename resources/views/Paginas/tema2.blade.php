					<h2>Evolución de la función del sistema de información</h2>
				</header>
				<h4>1950 - 1960: procesamiento de datos electrónicos, sistema de procesamiento de transacciones</h4>
				<p>Durante este período, el papel de SI fue principalmente para realizar actividades como el procesamiento de transacciones, el mantenimiento de registros y la contabilidad. SI se utilizó principalmente para el procesamiento electrónico de datos.</p>
				<p><b>EDP</b> se describe como el uso de computadoras para registrar, clasificar, manipular y resumir datos. También se llama procesamiento de información o procesamiento de datos automático.</p>
				<p>Sistema de procesamiento de transacciones (TPS) fue el primer sistema computarizado desarrollado para procesar datos comerciales. TPS estaba principalmente dirigido al personal administrativo de una organización. El TPS inicial utilizaba datos de procesamiento por lotes que se acumularon durante un período y todas las transacciones se procesaron posteriormente.</p>
				<p>TPS recopila, almacena, modifica y recupera las transacciones diarias de una organización. Por lo general, TPS informatiza o automatiza un proceso manual existente para permitir un procesamiento más rápido, un mejor servicio al cliente y costos administrativos reducidos. Ejemplos de productos de TPS son los depósitos en efectivo, el cajero automático (ATM), la orden de pago y los sistemas contables. TPS también se conoce como procesamiento de transacciones o procesamiento en tiempo real.</p>
				<br>
				<h4>1960 a 1970: Sistemas de información gerencial</h4>
				<p>Durante esta era, el rol de SI evolucionó desde TPS hasta Management Information Systems (MSI) o Sistemas de información gerenciales. MSI procesa los datos en informes informativos útiles y proporciona a los gerentes las herramientas para organizar evaluar y administrar de manera eficiente los departamentos dentro de una organización. MSI entrega información en forma de pantallas e informes preespecificados para respaldar la toma de decisiones empresariales. Algunos ejemplos de resultados de MSI son los sistemas de informes de tendencias de costos, análisis de ventas y rendimiento de producción.</p>
				<p>Por lo general, MSI genera tres tipos básicos de información que son:</p>
				<ul>
					<li>Los informes detallados de información típicamente confirman las actividades de procesamiento de transacciones. Un informe de pedido detallado es un ejemplo de informe detallado.</li>
					<li>La información resumida establece datos en un formato que un individuo puede revisar rápido y fácilmente</li>
					<li>Información mediante el filtrado de datos es uno de excepción. Los informes de excepciones ayudan a los gerentes a ahorrar tiempo porque no tienen que buscar en un informe detallado las excepciones.</li>
				</ul>
				<p>Este período también marcó el desarrollo cuando el enfoque de las organizaciones cambió lentamente de la mera automatización de los procesos comerciales básicos a la consolidación del control dentro de la función de procesamiento de datos.</p>
				<br>
				<h4>1970 a 1980: Sistemas de soporte de decisiones</h4>
				<p>En esta era, un avance importante fue la introducción de las computadoras personales (PC). Con la introducción de las PC, se produjo la distribución de la potencia informática o de procesamiento en toda la organización. La función de SI está fuertemente asociada con la administración en lugar de un enfoque técnico en una organización. El papel se centró en el "sistema interactivo basado en computadoras" para ayudar a los responsables de la toma de decisiones a resolver problemas.</p>
				<p>Este nuevo rol de los sistemas de información para proporcionar soporte ad-hoc interactivo para el proceso de toma de decisiones a los gerentes y otros profesionales de negocios se denomina Sistemas de Soporte de Decisiones (DSS). DSS sirve el nivel de planificación, gestión y operaciones de una organización, por lo general, la alta dirección.</p>
				<p>DSS usa datos de fuentes internas y / o externas. Las fuentes internas de datos pueden incluir datos de inventario, ventas, fabricación o financieros de la base de datos de una organización. Las fuentes externas podrían incluir precios, tasas de interés, población o tendencias. Los gerentes usan DSS para manipular los datos y ayudar con las decisiones. Los ejemplos de DSS son cifras de ingresos proyectadas basadas en suposiciones de ventas de nuevos productos, precios de productos y sistemas de análisis de riesgos.</p>
				<br>
				<h4>1980 a 1990: Sistemas de información ejecutivo</h4>
				<p>Este período dio lugar a la informática departamental debido a que muchas organizaciones compran su propio hardware y software para satisfacer sus necesidades departamentales. En lugar de esperar el apoyo indirecto del departamento de servicio corporativo centralizado, los empleados podrían usar sus propios recursos para respaldar sus requisitos laborales. Esta tendencia dio lugar a nuevos desafíos de incompatibilidad de datos, integridad y conectividad en diferentes departamentos. Además, los altos ejecutivos no usaban DSS ni MSI, por lo que se desarrollaron sistemas de información ejecutivo (ESI) o sistemas de soporte ejecutivo (ESS).</p>
				<p>ESI ofrece servicios de toma de decisiones a los ejecutivos a través del suministro de información interna y externa relevante para cumplir los objetivos estratégicos de la organización. A veces se consideran como una forma específica de DSS. Los ejemplos del ESI son sistemas para facilitar el acceso a las acciones de todos los competidores, desarrollos económicos para respaldar la planificación estratégica y el análisis del desempeño del negocio.</p>
				<br>
				<h4>1990 a 2000: Sistemas de gestión del conocimiento</h4>
				<p>Durante esta era, el rápido crecimiento de las intranets, extranets, internet y otras redes globales interconectadas cambiaron drásticamente las capacidades de SI en los negocios. Se hizo posible circular el conocimiento a diferentes partes del mundo independientemente del tiempo y el espacio.</p>
				<p>Este período también vio una aparición de sistemas de planificación de recursos empresariales (ERP). ERP es una forma específica de organización de un sistema de información estratégica que incorpora todos los componentes de una organización, incluidas la fabricación, las ventas, la gestión de recursos, la planificación de recursos humanos y el marketing.</p>
				<p>Además, hubo un avance en el desarrollo y la aplicación de técnicas de inteligencia artificial (IA) a los sistemas de información comercial. Sistemas expertos (ES) y sistemas de gestión del conocimiento (KMS) interconectados entre sí.</p>
				<p>Los sistemas expertos (ES) son un sistema informático que imita la capacidad de toma de decisiones de los expertos humanos. Por ejemplo, sistemas que hacen predicciones financieras, diagnostican enfermedades humanas y programan rutas para vehículos de reparto. El sistema de gestión del conocimiento (KMS) es un sistema de TI que almacena y recupera conocimiento para apoyar la creación, organización y diseminación del conocimiento empresarial dentro de la empresa. Los ejemplos de KMS son la base de datos de retroalimentación y los sistemas de mesa de ayuda.</p>
				<p>ES utiliza datos de los Sistemas de Gestión del Conocimiento para generar un resultado deseable del sistema de información, por ejemplo, un sistema de aprobación de solicitudes de préstamo</p>
				<br>
				<h4>2000 - presente: E-Business</h4>
				<p>Internet y las tecnologías y aplicaciones relacionadas cambiaron la forma en que operan las empresas y las personas trabajan. Las funciones de los sistemas de información en este período siguen siendo las mismas que hace 50 años: mantenimiento de registros, gestión de informes, procesamiento de transacciones, gestión de soporte y procesos de gestión de la organización. Se usa para respaldar el proceso comercial, la toma de decisiones y las ventajas competitiva.</p>
				<p>La diferencia es una mayor conectividad entre los componentes del sistema similares y diferentes. Existe una gran infraestructura de red, un mayor nivel de integración de funciones en todas las aplicaciones y potentes máquinas con mayor capacidad de almacenamiento. Muchas empresas usan tecnologías de Internet y procesos de negocio habilitados para la web para crear aplicaciones innovadoras de comercio electrónico.</p>
